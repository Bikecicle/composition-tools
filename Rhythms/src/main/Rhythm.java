package main;

public class Rhythm {
	
	String sample;
	Timbre[] timbres;
	Sequence[] sequences;
	int voiceCount;
	
	public Rhythm(String sample, Timbre[] timbres, Sequence[] sequences, int voiceCount) {
		this.sample = sample;
		this.timbres = timbres;
		this.sequences = sequences;
		this.voiceCount = voiceCount;
	}
	
	

}
